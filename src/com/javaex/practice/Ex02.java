package com.javaex.practice;

public class Ex02 {
	public static void main(String[] args) {
		
		System.out.println( 12 / 5 - 3); // 가독성을 위해서 괄호 추가해준다 ((12 / 5) - 3)) 12 / 5 == 2, 2 - 3 == -1
		System.out.println( 5 + 19 % 3 ); // 가독성을 위해서 괄호 추가해준다 (5 + (19 % 3)) --> 19 % 3 == 1, 5 + 1 == 6
		
		// 정답!!!
	}

}
